    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $post = Post::find($id);
        $post->likes()->delete();
        //$post->shares()->delete();
        $post->delete();

        return redirect()->route('posts.profile',Auth::id());
    }
